def seed_database(db: Session) -> None:
    for scenario_data in SCENARIOS:
        scenario = db.query(Scenario).filter(Scenario.slug == scenario_data["slug"]).first()
        if scenario is None:
            scenario = Scenario(
                slug=scenario_data["slug"],
                title=scenario_data["title"],
                theme=scenario_data["theme"],
                difficulty=scenario_data["difficulty"],
                description=scenario_data["description"],
                is_playable=scenario_data["is_playable"],
            )
            db.add(scenario)
            db.flush()

        scenario.title = scenario_data["title"]
        scenario.theme = scenario_data["theme"]
        scenario.difficulty = scenario_data["difficulty"]
        scenario.description = scenario_data["description"]
        scenario.is_playable = scenario_data["is_playable"]

        has_sessions = db.query(GameSession.id).filter(GameSession.scenario_id == scenario.id).first() is not None
        if has_sessions:
            continue

        for existing_step in list(scenario.steps):
            db.delete(existing_step)
        db.flush()

        for step_data in scenario_data["steps"]:
            step = ScenarioStep(
                scenario_id=scenario.id,
                step_order=step_data["step_order"],
                prompt=step_data["prompt"],
                threat_type=step_data["threat_type"],
                explanation=step_data["explanation"],
            )
            db.add(step)
            db.flush()

            for option_data in step_data["options"]:
                db.add(
                    DecisionOption(
                        step_id=step.id,
                        label=option_data["label"],
                        is_correct=option_data["is_correct"],
                        hp_delta=option_data["hp_delta"],
                        hint=option_data["hint"],
                        consequence_text=option_data["consequence_text"],
                    )
                )

    db.commit()
