export const dynamic = "force-dynamic";

export default function ForUsersPage() {
  const [activeGuideId, setActiveGuideId] = useState<string | null>(null);
  const [isClient, setIsClient] = useState(false);
  const activeGuide = useMemo(() => guideCards.find((card) => card.id === activeGuideId) || null, [activeGuideId]);

  useEffect(() => {
    setIsClient(true);
  }, []);

  useEffect(() => {
    if (!activeGuideId) {
      document.body.style.overflow = "";
      return;
    }

    const previousOverflow = document.body.style.overflow;
    document.body.style.overflow = "hidden";

    return () => {
      document.body.style.overflow = previousOverflow;
    };
  }, [activeGuideId]);

  return (
    <div className="shell shell-wide space-y-10 py-12">
      <SectionTitle
        eyebrow="Для пользователей"
        title="Практические правила цифровой безопасности с объяснением механики атак"
        description="Здесь собраны понятные разборы типовых атак: как именно работает схема, по каким признакам её заметить и что безопаснее сделать в первые минуты."
      />

      <div className="guide-hero-card">
        <div className="feature-icon">
          <ShieldCheck size={18} />
        </div>
        <div>
          <h2 className="text-2xl font-semibold text-[var(--color-text-primary)]">Что важно запомнить прямо сейчас</h2>
          <p className="body-copy mt-3 max-w-3xl">
            Если вас торопят, требуют код, подсовывают QR, вынуждают поставить “защитное” приложение или давят авторитетом знакомого голоса, это уже красный флаг. Нажмите на карточку ниже: внутри коротко разобраны и механизм атаки, и безопасный порядок действий.
          </p>
        </div>
      </div>

      <div className="guide-card-grid">
        {guideCards.map((card, index) => (
          <button key={card.id} type="button" className="guide-card" onClick={() => setActiveGuideId(card.id)}>
            <div className="guide-card-top">
              <span className="guide-card-index">0{index + 1}</span>
              <span className="guide-card-kicker">{card.kicker}</span>
            </div>
            <h3 className="guide-card-title">{card.title}</h3>
            <p className="guide-card-summary">{card.summary}</p>
          </button>
        ))}
      </div>

      {isClient && activeGuide
        ? createPortal(
            <div className="guide-modal-backdrop" onClick={() => setActiveGuideId(null)}>
              <div className="guide-modal" onClick={(event) => event.stopPropagation()}>
                <button type="button" className="icon-button guide-modal-close" onClick={() => setActiveGuideId(null)}>
                  <X size={18} />
                </button>
                <p className="eyebrow">{activeGuide.kicker}</p>
                <h2 className="mt-4 text-3xl font-semibold text-[var(--color-text-primary)]">{activeGuide.title}</h2>
                <p className="body-copy mt-4 text-sm">{activeGuide.summary}</p>

                <article className="soft-tile mt-6 guide-modal-mechanism">
                  <div className="feature-icon">
                    <Smartphone size={18} />
                  </div>
                  <div>
                    <h3 className="mt-1 text-lg font-semibold text-[var(--color-text-primary)]">Как работает атака</h3>
                    <p className="body-copy mt-3 text-sm">{activeGuide.mechanism}</p>
                  </div>
                </article>

                <div className="guide-modal-grid">
                  <article className="soft-tile guide-modal-tile">
                    <div className="feature-icon">
                      <ShieldCheck size={18} />
                    </div>
                    <h3 className="mt-4 text-lg font-semibold text-[var(--color-text-primary)]">Что делать</h3>
                    <p className="body-copy mt-3 text-sm">{activeGuide.safeAction}</p>
                  </article>

                  <article className="soft-tile guide-modal-tile guide-modal-tile-alert">
                    <div className="feature-icon guide-modal-alert-icon">
                      <AlertTriangle size={18} />
                    </div>
                    <h3 className="mt-4 text-lg font-semibold text-[var(--color-text-primary)]">Что будет, если нарушить правило</h3>
                    <p className="body-copy mt-3 text-sm">{activeGuide.riskResult}</p>
                  </article>
                </div>
              </div>
            </div>,
            document.body
          )
        : null}

      <div className="grid gap-4 md:grid-cols-3">
        <div className="soft-tile">
          <div className="feature-icon">
            <Smartphone size={18} />
          </div>
          <h3 className="mt-4 text-lg font-semibold text-[var(--color-text-primary)]">Телефон и мессенджеры</h3>
          <p className="body-copy mt-2 text-sm">Главный триггер атаки — срочность. Если вас торопят, запугивают или требуют секрет прямо сейчас, это уже повод остановиться.</p>
        </div>
        <div className="soft-tile">
          <div className="feature-icon">
            <Wifi size={18} />
          </div>
          <h3 className="mt-4 text-lg font-semibold text-[var(--color-text-primary)]">Сети и маршруты</h3>
          <p className="body-copy mt-2 text-sm">Проверяйте не только название сети, но и весь путь: домен, форму входа, приложение, QR и запрос на доступ.</p>
        </div>
        <div className="soft-tile">
          <div className="feature-icon">
            <ShieldCheck size={18} />
          </div>
          <h3 className="mt-4 text-lg font-semibold text-[var(--color-text-primary)]">Аккаунты и восстановление</h3>
          <p className="body-copy mt-2 text-sm">Самый дешёвый и сильный набор защиты всё ещё один и тот же: уникальные пароли, 2FA и быстрая реакция на подозрительные события.</p>
        </div>
      </div>
    </div>
  );
}
